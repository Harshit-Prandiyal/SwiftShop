class Product {
  constructor(id, title, description, price, imageUrl,category , rating , count) {
    this.id = id;
    this.title = title;
    this.description = description;
    this.price = price;
    this.imageUrl = imageUrl;
    this.category = category;
    this.rating = rating;
    this.count = count;
  }
}
export default Product;
